export const description = `This graduate seminar examines video games as sophisticated philosophical texts that engage with fundamental questions about technology, consciousness, identity, and environmental ethics. Drawing on new media theory, digital humanities methodologies, and game studies scholarship, we will analyze how interactive digital narratives create unique opportunities for philosophical engagement that distinguish them from traditional literary forms. Through close readings of contemporary games—with special attention to titles like Atomic Heart—alongside theoretical frameworks from McLuhan, Haraway, Winner, and others, students will develop critical approaches to understanding games as cultural artifacts worthy of serious humanistic inquiry.

The course explores how the interactive medium fundamentally transforms narrative meaning-making, creating experiential dimensions that passive media cannot replicate. Students will investigate the convergence of literary game studies, environmental philosophy, artificial intelligence ethics, and cyborg theory while developing original research that contributes to emerging scholarly conversations about digital texts in the 21st century.`;

